function getWednesdayDate(weeksOffset: number, hour = 19, minute = 0): string {
  const now = new Date()
  const today = now.getDay()
  const daysUntilWednesday = (3 - today + 7) % 7
  const nextWednesday = new Date(now)
  nextWednesday.setDate(now.getDate() + daysUntilWednesday + (weeksOffset * 7))
  nextWednesday.setHours(hour, minute, 0, 0)
  return nextWednesday.toISOString()
}

function getThursdayDate(weeksOffset: number, hour = 18, minute = 0): string {
  const now = new Date()
  const today = now.getDay()
  const daysUntilThursday = (4 - today + 7) % 7
  const nextThursday = new Date(now)
  nextThursday.setDate(now.getDate() + daysUntilThursday + (weeksOffset * 7))
  nextThursday.setHours(hour, minute, 0, 0)
  return nextThursday.toISOString()
}

